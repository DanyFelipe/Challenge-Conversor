    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {

            Temperatura temp1 = lista.get(jComboBox1.getSelectedItem());
            Temperatura temp2 = lista.get(jComboBox2.getSelectedItem());
            double valor = Double.parseDouble(jTextField1.getText());

            jTextField2.setText(temp1.convertir(temp2, valor));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ingrese los datos correctamente", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

